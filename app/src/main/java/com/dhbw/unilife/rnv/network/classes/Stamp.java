package com.dhbw.unilife.rnv.network.classes;

public class Stamp {
    String date;
    String time;
    String timeSec;
    String rtDate;
    String rtTime;
    String rtTimeSec;

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }

    public String getTimeSec() {
        return timeSec;
    }

    public String getRtDate() {
        return rtDate;
    }

    public String getRtTime() {
        return rtTime;
    }

    public String getRtTimeSec() {
        return rtTimeSec;
    }
}
