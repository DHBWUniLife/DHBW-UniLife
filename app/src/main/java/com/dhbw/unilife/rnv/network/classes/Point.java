package com.dhbw.unilife.rnv.network.classes;

public class Point {
    String name;
    String nameWO;
    String platformName;
    String place;
    String nameWithPlace;
    String usage;
    String pointType;
    String omc;
    String placeID;
    String desc;
    DateTime dateTime;
    Stamp stamp;
    Ref ref;

    public String getName() {
        return name;
    }

    public String getNameWO() {
        return nameWO;
    }

    public String getPlatformName() {
        return platformName;
    }

    public String getPlace() {
        return place;
    }

    public String getNameWithPlace() {
        return nameWithPlace;
    }

    public String getUsage() {
        return usage;
    }

    public String getPointType() {
        return pointType;
    }

    public String getOmc() {
        return omc;
    }

    public String getPlaceID() {
        return placeID;
    }

    public String getDesc() {
        return desc;
    }

    public DateTime getDateTime() {
        return dateTime;
    }

    public Stamp getStamp() {
        return stamp;
    }

    public Ref getRef() {
        return ref;
    }
}
